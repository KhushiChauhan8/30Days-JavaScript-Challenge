// 1. Variable Types Console Log: Declare variables of different data types and log both the value and type of each variable to the console

var num = 456;
let str = "Feature String";
const bool = true;
let obj = { title: "Dev", exp: 5 };
const arr = ["html", "Css", "js"];

console.log(" num =", num, ", typeof =", typeof num); // Output: 456, number

console.log(" str =", str, ", typeof =", typeof str); // Output: Feature String, string

console.log(" bool =", bool, ", typeof =", typeof bool); // Output: true, boolean

console.log(" obj =", obj, ", typeof =", typeof obj); // Output: {title: "Dev", exp: 5}, object

console.log(" arr =", arr, ", typeof =", typeof arr); // Output: ["html", "css", "js"], object