// Activity 2: Assignment Operators

// Task 6: Use the += operator to add a number to a variable and log the result to the console

let number = 10;
number += 5;
console.log(`The result after adding 5 to the variable is ${number}`); // The result after adding 5 to the variable is 15

// Task 7: Use the -= operator to subtract a number from a variable and log the result to the console

let anotherNumber = 20;
anotherNumber -= 7;
console.log(`The result after subtracting 7 from the variable is ${anotherNumber}`); // The result after subtracting 7 from the variable is 13
